  console.log('Popup pour:' + id)

  let chemin = "./assets/personnages/";

  let imageperso = document.getElementById('imageperso');
  console.log(imageperso.src);
  let LowerCase_replace = profil[id].prenom.toLowerCase().replace('é', 'e').replace(' ', '')
